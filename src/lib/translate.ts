// Detect if text contains Spanish words
function hasSpanish(text: string): boolean {
  const spanishIndicators = /\b(necesitamos|queremos|podemos|precio|calidad|envﾃｭo|entrega|muestra|tambiﾃｩn|ademﾃ｡s|por favor|buena|mala|pedido|pago|factura|presupuesto|disponible|incluido|garantﾃｭa|certificado|embalaje|urgente|importante|obligatorio|opcional|descuento|oferta|reclamaciﾃｳn|incidencia|comprobar|confirmar|revisar)\b/i
  return spanishIndicators.test(text)
}

export function translateToEnglish(text: string): string {
  if (!text || !text.trim()) return text
  if (!hasSpanish(text)) return text

  let result = text
  for (const [pattern, replacement] of dictionary) {
    result = result.replace(pattern, replacement)
  }
  return result
}
